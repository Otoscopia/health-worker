class SchoolEntity {
  // school id
  final String id;

  // school name
  final String name;

  // school abbreviation
  final String abbr;

  // school code
  final String code;

  // school address
  final String address;

  SchoolEntity({
    required this.id,
    required this.name,
    required this.abbr,
    required this.code,
    required this.address,
  });
}